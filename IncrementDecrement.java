package unary.operaters;

public class IncrementDecrement {

	public static void main(String[] args) {
		int a,b;
		a=20;
		b=++a;
		System.out.println("a="+a+",b="+b);
		
		a=30;
		b=a++;
		System.out.println("a="+a+",b="+b);
		
		a=12;
		b=--a;
		System.out.println("a="+a+",b="+b);
		
		a=10;
		b=a--;
		System.out.println("a="+a+",b="+b);
		
		
		
		
	}

}
